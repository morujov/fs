<?php

namespace App\Services\Moderation;

/**
 * Исход одного правила конвейера.
 */
enum RuleOutcome: string
{
    /** Претензий нет. */
    case Pass = 'pass';

    /** Подозрительно. Добавляет к score; при превышении порога — ручная очередь. */
    case Flag = 'flag';

    /** Отказ. Объявление не опубликуется, пока продавец не исправит. */
    case Reject = 'reject';

    /**
     * Не готово, но и не ошибка.
     *
     * Отдельный исход намеренно. Непройденный OTP — не претензия к
     * объявлению: продавец ничего плохого не сделал, он просто ещё не
     * ввёл код. Считать это `reject` значило бы показать ему «отклонено»
     * за то, что он в процессе. Считать `flag` — отправить в ручную
     * очередь модератору, которому там делать нечего.
     */
    case Hold = 'hold';
}
